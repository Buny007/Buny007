"use client"
import { useState, useEffect } from "react"
import MobileFieldApp from "./mobile"

export default function ResponsiveApp() {
  const [isMobile, setIsMobile] = useState(false)
  const [isClient, setIsClient] = useState(false)

  useEffect(() => {
    setIsClient(true)
    const checkMobile = () => {
      setIsMobile(window.innerWidth < 768)
    }

    checkMobile()
    window.addEventListener("resize", checkMobile)

    return () => window.removeEventListener("resize", checkMobile)
  }, [])

  // Warten auf Client-side Rendering
  if (!isClient) {
    return (
      <div className="min-h-screen bg-gray-100 flex items-center justify-center">
        <div className="text-center">
          <div className="animate-spin rounded-full h-32 w-32 border-b-2 border-blue-600 mx-auto"></div>
          <p className="mt-4 text-gray-600">HB Montageservice wird geladen...</p>
        </div>
      </div>
    )
  }

  // Immer mobile App anzeigen
  return <MobileFieldApp />
}
